    private void jButton1SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1SalvarActionPerformed
        String nome = jTextField1Nome.getText();
        String cpf = jFormattedTextField2Cpf.getText();
        String email = jTextField1Email.getText();
        String endereco = jTextField2Endereco.getText();
        boolean sucesso;
        try{
        Date birthDate = sdf.parse(jFormattedTextField1DtNascimento.getText());
        ClienteController clienteC = new ClienteController();
        if(codCliente==0){
            sucesso = clienteC.cadastraCliente(nome, cpf, email, endereco, birthDate);
            if(sucesso){
                JOptionPane.showMessageDialog(rootPane, "Cliente cadastrado com sucesso");
            }else{
                JOptionPane.showMessageDialog(rootPane, "Erro ao cadastrar cliente, verifique se informa√ß√µes est√£o corretas");
            }
        }else{
            sucesso = clienteC.atualizaCliente(codCliente, nome, cpf, email, endereco, birthDate);
            if(sucesso){
                JOptionPane.showMessageDialog(rootPane, "Cliente ATUALIZADO com sucesso");
            }else{
                JOptionPane.showMessageDialog(rootPane, "Erro ao atualizar cliente, verifique se informa√ß√µes est√£o corretas");
            }
        }
        }catch(ParseException p){
            JOptionPane.showMessageDialog(rootPane, "Data informada inv√°lida");
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }//GEN-LAST:event_jButton1SalvarActionPerformed

